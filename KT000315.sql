/* VTMCLH cliente activos
DECLARE @CODEMP VARCHAR (6), @CODTRA VARCHAR (12), @NROLOT VARCHAR (3)
SET @NROLOT = '59'
SET @CODTRA = 'KT315'
SET @CODEMP = 'L2001'

*/
SELECT
 RIGHT(SPACE(11)+REPLACE(ISNULL(VTMCLH_NRODOC,' '),'-',''),11) FLD001,
 RIGHT('00000'+CONVERT(VARCHAR,VTMCLH_NROCTA),6) FLD002,
 SPACE(20) FLD003,
 RIGHT(SPACE(40)+VTMCLH_NOMBRE,40) FLD004,
 SPACE(20) FLD005,
 RIGHT(SPACE(40)+VTMCLH_DIRECC,40) FLD006,
 RIGHT(SPACE(20)+GRTPAC_DESCRP,20) FLD007,
 RIGHT(SPACE(20)+GRTJUR_DESCRP,20) FLD008,
 RIGHT('00000'+VTMCLH_CODPOS,5) FLD009,
 RIGHT(SPACE(5)+VTMCLH_CODPAI,5) FLD0010,
 (CONVERT(VARCHAR,VTMCLH_NROCTA))+CONVERT(VARCHAR,1) FLD099 /*Orden de Registro*/
FROM VTMCLH
INNER JOIN GRTPAC ON GRTPAC_CODPOS = VTMCLH_CODPOS
INNER JOIN GRTJUR ON GRTJUR_JURISD = VTMCLH_JURISD
WHERE 
VTMCLH_DEBAJA = 'N'
